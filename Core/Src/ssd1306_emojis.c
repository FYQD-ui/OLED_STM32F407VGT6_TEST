const uint8_t* emoji_list[] = {
    emoji_happy_bitmap,
    emoji_sad_bitmap,
    emoji_confused_bitmap,
    emoji_crying_bitmap,
    emoji_unamused_bitmap
};

// 表情数量
#define EMOJI_COUNT (sizeof(emoji_list) / sizeof(emoji_list[0]))

// OLED 显示表情函数
void ssd1306_DisplayEmoji(uint8_t index) {
    if (index >= EMOJI_COUNT) return;  // 防止溢出
    ssd1306_Fill(Black);  // 清屏
    ssd1306_DrawBitmap(0, 0, emoji_list[index], 128, 64, White);
    ssd1306_UpdateScreen();
}

// 循环显示表情，每 3 秒切换一次
void ssd1306_ShowEmojisLoop(void) {
    uint8_t index = 0;
    while (1) {
        ssd1306_DisplayEmoji(index);
        HAL_Delay(1000);  // 3 秒
        index = (index + 1) % EMOJI_COUNT;
    }
}
